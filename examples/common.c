nk_bool style_combo(struct nk_context *ctx, float item_height, float list_width, int *style) {
    struct nk_vec2 list_size = { list_width, NUM_STYLES * (nk_widget_height(ctx) + ctx->style.combo.content_padding.y * 2)};
    float list_height = NUM_STYLES * ctx->style.combo.content_padding.y;
    int new_style = nk_combo(ctx, STYLE_LABELS, NUM_STYLES, *style, item_height, list_size);
    return (new_style != *style) ? (*style = new_style, nk_true) : nk_false;
}
